fn main() {
    // Сложение
    let sum = 5 + 10;
    println!("{sum}");
    // Вычитание
    let difference = 95.5 - 4.3;
    println!("{difference}");
    // Умножение
    let product = 4 * 30;
    println!("{product}");
    // Деление
    let quotient = 56.7 / 32.2;
    println!("{quotient}");
    let truncated = -5 / 3; // Results in -1
    println!("{truncated}");
    // Остаток
    let remainder = 43 % 5;
    println!("{remainder}");
}
